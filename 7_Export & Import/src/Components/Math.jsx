pi = 3.14;

function doublepi() {
  return pi * 2;
}

function triplepi() {
  return pi * 3;
}
export default pi;
export { doublepi, triplepi };

function add(n1, n2) {
  return n1 + n2;
}

function multiply(n1, n2) {
  return n1 * n2;
}

function subtract(n1, n2) {
  return n1 - n2;
}

function divide(n1, n2) {
  return n1 / n2;
}

export { add, subtract, multiply, divide };
